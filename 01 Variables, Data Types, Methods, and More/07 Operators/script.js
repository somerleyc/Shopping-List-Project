// 1. Arithmetic Operators

let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 5 % 5;
x = 7 % 5;

// Concatenation Operator (+)
x = 'Hello' + ' ' + 'World';

// Exponent Operator (to the power of **)
x = 2 ** 3;

// Increment Operator
x = 1;
// x = x + 1;
x++;

// Decrement Operator
// x = x - 1;
x--;

// 2. Assignment Operators

x = 10;

x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;

// 3. Comparison Operators

// == evaluates value
x = 2 == 2;
// === evaluates value AND type
x = 2 === 2;
// != is opposite of ==
x = 2 != 2;
// !== is opposite of ===
x = 2 !== '2';

x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

console.log(x);